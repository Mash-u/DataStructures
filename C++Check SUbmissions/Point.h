#ifndef POINT
#define POINT

class Point
{
	public:

		/************************************************************************************************************************************************************
		* NAME         : Point()
		* DESCRIPTION  : Defualt constructore used when Point object is instantiated without being intialized. X and Y will be set to 0.
		* RETURN VALUE : no return value
		* -----------------------------------------------------------------------------------------------------------------------------------------------------------*
		*************************************************************************************************************************************************************/
		Point();  

		/************************************************************************************************************************************************************
		* NAME         : Point(double x, double y)
		* DESCRIPTION  : Constructor used to initialize a Point Object. Pass 2 double values as arguments. They will be asigned to x and y.
		* RETURN VALUE : no return value
		* -----------------------------------------------------------------------------------------------------------------------------------------------------------*
		*************************************************************************************************************************************************************/
		Point(double x, double y);  

		/************************************************************************************************************************************************************
		* NAME         : setXCoordinate(double)
		* DESCRIPTION  : set method used to modify the value of instance variable member x. Accepts double value.
		* RETURN VALUE : no return value
		* -----------------------------------------------------------------------------------------------------------------------------------------------------------*
		*************************************************************************************************************************************************************/
		void setXCoordinate(double); 

		/************************************************************************************************************************************************************
		* NAME         : setYCoordinate(double)
		* DESCRIPTION  : set method used to modify the value of instance variable member y. Accepts double value.
		* RETURN VALUE : no return value
		* -----------------------------------------------------------------------------------------------------------------------------------------------------------*
		*************************************************************************************************************************************************************/
		void setYCoordinate(double); 

		/************************************************************************************************************************************************************
		* NAME         : getXCoordinate()
		* DESCRIPTION  : get method used to return the value of instance variable member x.
		* RETURN VALUE : double
		* -----------------------------------------------------------------------------------------------------------------------------------------------------------*
		*************************************************************************************************************************************************************/
		double getXCoordinate();     

		/************************************************************************************************************************************************************
		* NAME         : getYCoordinate()
		* DESCRIPTION  : get method used to return the value of instance variable member y.
		* RETURN VALUE : double 
		* -----------------------------------------------------------------------------------------------------------------------------------------------------------*
		*************************************************************************************************************************************************************/
		double getYCoordinate();     

		/************************************************************************************************************************************************************
		* NAME         : distance()
		* DESCRIPTION  : method is used to calculate the (x.y) coordinate distance from the origin (0,0). It will return the ditance in the form of a double.
		* RETURN VALUE : double.
		* -----------------------------------------------------------------------------------------------------------------------------------------------------------*
		*************************************************************************************************************************************************************/
		double distance();   

    private:
		double x;                    // double var for x coordinate.
		double y;                    // double var for y coordinate.
};

#endif