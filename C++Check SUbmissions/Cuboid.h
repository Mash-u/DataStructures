#ifndef CUBOID
#define CUBOID

class Cuboid {
	public:
		/****************************************************************************************************************************************************************
		* NAME         : Cuboid()
		* DESCRIPTION  : Defualt constructor for Cuboid Class. Instance member variables height, lenth, and width are intitialized to 0;
		* RETURN VALUE : no return value
		* --------------------------------------------------------------------------------------------------------------------------------------------------------------*
		****************************************************************************************************************************************************************/
		Cuboid();

		/****************************************************************************************************************************************************************
		* NAME         : Cuboid(double height, double length, double width)
		* DESCRIPTION  : Constructor for Cuboid Class which takes 3 double variables as parameters. These 3 values are passed to the setDimensions() method.
		* RETURN VALUE : no return value
		* --------------------------------------------------------------------------------------------------------------------------------------------------------------*
		****************************************************************************************************************************************************************/
		Cuboid(double height, double length, double width);

		/****************************************************************************************************************************************************************
		* NAME         : setDimensions(double newHeight, double newLength, double newWidth)
		* DESCRIPTION  : THis is a setMethod which takes 3 double values, and assigns those values to the 3 instance variable members of the Cuboid Class.
		* RETURN VALUE : void
		* --------------------------------------------------------------------------------------------------------------------------------------------------------------*
		****************************************************************************************************************************************************************/
		void setDimensions(double newHeight, double newLength, double newWidth);

		/****************************************************************************************************************************************************************
		* NAME         : getHeight()
		* DESCRIPTION  : Get method that returns the value of instance variable height.
		* RETURN VALUE : double
		* --------------------------------------------------------------------------------------------------------------------------------------------------------------*
		****************************************************************************************************************************************************************/
		double getHeight();

		/****************************************************************************************************************************************************************
		* NAME         : getLength()
		* DESCRIPTION  : Get method that returns the value of isntance variable length.
		* RETURN VALUE : double
		* --------------------------------------------------------------------------------------------------------------------------------------------------------------*
		****************************************************************************************************************************************************************/

		double getLength();

		/****************************************************************************************************************************************************************
		* NAME         : getWidth()
		* DESCRIPTION  : Get method that returns the value of isntance variable width.
		* RETURN VALUE : double
		* --------------------------------------------------------------------------------------------------------------------------------------------------------------*
		****************************************************************************************************************************************************************/
		double getWidth();

		/****************************************************************************************************************************************************************
		* NAME         : volume()
		* DESCRIPTION  : This method returns the volume of the Cuboid object (uses the instance member variables height, length, and width).
		* RETURN VALUE : double
		* --------------------------------------------------------------------------------------------------------------------------------------------------------------*
		****************************************************************************************************************************************************************/
		double volume();

		/****************************************************************************************************************************************************************
		* NAME         : surfaceArea()
		* DESCRIPTION  : This method returns the surface area of the Cuboid object. (uses the instance member variables height, length, width)
		* RETURN VALUE : double
		* --------------------------------------------------------------------------------------------------------------------------------------------------------------*
		****************************************************************************************************************************************************************/
		double surfaceArea();

		/****************************************************************************************************************************************************************
		* NAME         : increaseDimension(double factor)
		* DESCRIPTION  : This method takes in a double value represenenting the value that the user would like to factor the Cuboids dimensions by. Muliplies the three
		*                instance member variables by this value.
		* RETURN VALUE : void
		* --------------------------------------------------------------------------------------------------------------------------------------------------------------*
		****************************************************************************************************************************************************************/
		void increaseDimension(double factor);
	
     private:
		 double height;
		 double length;
		 double width;



};
#endif